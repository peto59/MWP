namespace MWP.BackEnd.Player
{
    /// <summary>
    /// Enumerator for all loop states
    /// </summary>
    public enum LoopState
    {
        /// <summary>
        /// no looping
        /// </summary>
        None,
        /// <summary>
        /// looping through entire queue
        /// </summary>
        All,
        /// <summary>
        /// looping of single song
        /// </summary>
        Single
    }
}